    <div class="card">
        <p>
            O LAMPEH estabeleceu vários convênios, termos cooperação e contratos de prestação de serviços desde sua
            criação, em 2004. A cooperação com outras instituições, mas também com instâncias da própria universidade, é
            uma de suas prioridades.
        </p>
        <p>
            A primeira cooperação foi realizada com o Departamento de Economia Doméstica da UFV, através da qual foram
            executados trabalhos de conservação preventiva, identificação e catalogação do acervo do professor Antônio
            Mendes, mais conhecido como Padre Mendes.
        </p>
        <p>
            Em seguida, estabeleceu convênio com o Instituto do Patrimônio Histórico e Artístico Nacional (IPHAN), por
            intermédio da Casa Setecentista de Mariana. Este convênio teve início em 2005 e se mantém até os dias
            atuais. Através dele foram realizadas medidas de conservação preventiva dos inventários dos Cartórios do 1º.
            e 2º. Ofício de Mariana, produzidos entre o do início do século XVIII ao início do século XX. São milhares
            de documentos relativos ao chamado Termo de Mariana, uma extensa região compreendida entre a então Vila de
            Mariana e a fronteira com o Rio de Janeiro.
        </p>
        <p>
            Além da conservação preventiva, a equipe do LAMPEH contribui para a melhoria da identificação dos documentos
            e para o acesso público aos mesmos, através de sistema informatizado que disponibiliza imagens digitalizadas
            dos documentos. Para a realização de um convênio tão importante, foram obtidos recursos financeiros junto a
            várias instituições de fomento, como FAPEMIG, CNPq e MEC/Proext. Mais recentemente, o IPHAN também aportou
            recursos, provenientes da empresa Vale S. A.
        </p>
        <p>
            Este trabalho acabou por gerar vários outros, como trabalhos de conclusão de curso de graduação,
            publicações, cursos de pós-graduação e dissertações de mestrado. Em 2022 teve início o Curso de
            Especialização em Preservação e Difusão de Estruturas e Sítios Arqueológicos, fruto de convênio com a
            empresa Vale S. A.
        </p>
        <p>
            Em 2023 será oferecido o Curso de Extensão em Identificação, Salvaguarda e Difusão do Patrimônio Cultural.
            Ambos os cursos são em formato EAD e conta equipes formadas por vários professores. A dissertação de Aline
            Nascimento Ribeiro, que atuou como bolsista em alguns dos projetos desenvolvidos pelo LAMPEH, resultou na
            candidatura pelo IPHAN do acervo do Cartório do 1º. Ofício de Mariana ao Programa Memória do Mundo,
            tornando-se o primeiro acervo documental deste órgão a fazer parte do Registro Nacional do Brasil no
            Programa Memória do Mundo (MOW) da UNESCO.
        </p>
        <p>
            Outro convênio importante foi estabelecido com a Câmara Municipal de Viçosa. Numa primeira etapa foram
            higienizados, identificados e classificados os documentos do arquivo histórico da Câmara, além de criado um
            sistema informatizado para as fichas de identificação dos mesmos. Atualmente (2022), encontra-se em fase de
            conclusão um segundo convênio, através do qual este trabalho foi refeito e expandido. O novo sistema
            informatizado disponibilizará imagens digitalizadas dos documentos, elaborou-se uma tabela de temporalidade
            para aplicação pelo órgão público e também o esboço de um sistema completo de arquivo, compreendo as três
            fases do chamado ciclo de vida dos documentos.
        </p>
        <p>
            O LAMPEH atuou também junto aos Fóruns de Piranga e Viçosa, realizando trabalhos de conservação preventiva e
            identificação de milhares de documentos produzidos entre o meado do século XIX e o meado do século XX.
            Apesar do esforço da equipe, constituída por professores e estudantes do Curso de História, não foi possível
            a digitalização dos documentos devido a entraves burocráticos. Com isso, o acesso público aos mesmos
            continua restrito a consultas presenciais.
        </p>
        <p>
            Com a Secretaria Municipal de Cultura, Patrimônio Histórico e Esportes de Viçosa foram firmados dois
            convênios. O primeiro, que contou com a participação de equipe da Biblioteca Central da UFV, consistiu na
            conservação preventiva e catalogação de cerca de 10 mil livros da Biblioteca Pública Municipal de Viçosa.
            Outro projeto, em andamento, utilizará a tecnologia do QR Code para identificar e difundir o patrimônio
            cultural de Viçosa.
        </p>
        <p>
            Em agosto de 2022, o LAMPEH negocia mais um convênio com administração municipal para a realização dos
            mesmos objetivos para os quais foi criado: a conservação preventiva de acervos e o acesso público aos
            mesmos. Um novo curso de pós-graduação também se encontra em fase de elaboração. Estas iniciativas visam
            contribuir para uma melhor formação dos estudantes envolvidos, mas principalmente a preservação e difusão do
            patrimônio cultural brasileiro, em especial dos acervos bibliográficos, arqueológicos e documentais.
        </p>
