        <p>O Laboratório Multimídia de Pesquisa Histórica (LAMPEH) foi criado em 2004 com a finalidade de contribuir
            para as pesquisas de estudantes e professores do Curso de História da Universidade Federal de Viçosa.
            Inicialmente, com o apoio do Centro de Ciências Humanas, Letras e Artes, adquiriu cópias de microfilmes de
            jornais mineiros e reuniu acervo em CD do Projeto Resgate Barão do Rio Branco, gentilmente doados pela Sra.
            Esther Bertoletti (<a target="_blank" href="https://bndigital.bn.gov.br/dossies/projeto-resgate-barao-do-rio-branco/">https://bndigital.bn.gov.br/dossies/projeto-resgate-barao-do-rio-branco/</a>).
        </p>
        <p>

            Com o passar do tempo o LAMPEH expandiu suas funções, passando também a realizar projetos de pesquisa,
            projetos de extensão e contratos de prestação de serviços na sua área de atuação, ou seja, conservação
            preventiva, identificação e difusão de acervos documentais (ver item Projetos desta homepage).

        </p>
        <p>

            Alguns destes projetos foram financiados pelas mais renomadas instituições de fomento como o CNPq, a FAPEMIG
            e o Ministério da Educação (PROEXT). O Laboratório executou também atividades de prestação de serviços para
            a Câmara Municipal de Viçosa e a Prefeitura Municipal de Viçosa. Atuou junto a várias instituições, como o
            Instituto do Patrimônio Histórico e Artístico Nacional (IPHAN), o Fórum da Comarca de Piranga e o Fórum da
            Comarca de Viçosa. Colaborou também com diversas instâncias da UFV, como o Departamento de Economia
            Doméstica, o Museu Casa Arthur Bernardes, a Pró-Reitoria de Extensão e Cultura, entre outros.</p>
        <p>
            Entre 2004 e 2015, o LAMPEH funcionou na Sala Multimídia da Biblioteca Central da UFV, prestando serviços de
            apoio a este órgão na consulta controlada de determinados itens do seu acervo. Em 2016, foi transferido para
            as instalações do Departamento de História, onde conta com estrutura de secretaria, apoio à pesquisa e para
            atividades de conservação preventiva de documentos. Desde sua criação o Laboratório é coordenado pelo
            professor Jonas Marçal de Queiroz, que de 2004 a 2015 contou com a colaboração do professor Fábio Faria
            Mendes nas atividades de coordenação e execução de pesquisas.
        </p>
